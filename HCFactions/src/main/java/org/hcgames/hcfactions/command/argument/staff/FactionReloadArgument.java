package org.hcgames.hcfactions.command.argument.staff;

import org.bukkit.ChatColor;
import org.bukkit.command.Command;
import org.bukkit.command.CommandSender;
import org.hcgames.hcfactions.HCFactions;
import technology.brk.util.command.CommandArgument;

import java.util.Collections;
import java.util.List;

public class FactionReloadArgument extends CommandArgument {

    private final HCFactions plugin;

    public FactionReloadArgument(HCFactions plugin) {
        super("reload", "Reloads the messages file from .");
        this.plugin = plugin;
        this.permission = "hcf.command.faction.argument." + getName();
    }

    @Override
    public String getUsage(String label) {
        return '/' + label + ' ' + getName();
    }

    @Override
    public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {
        //plugin.getMessages().reload();
        sender.sendMessage(ChatColor.RED + "Command disabled.");
        return true;
    }

    @Override
    public List<String> onTabComplete(CommandSender sender, Command command, String label, String[] args) {
        return args.length == 2 ? null : Collections.emptyList();
    }
}
